<h5>PROTECT YOUR PROPERTY</h5>
 
<p>
<strong><font style="font-size:18px;">Why should I have a security alarm system?</font></strong><br />
Having a security alarm system may decreases the chances of a burglary. Even if the alarm system does not keep a burglar from breaking in, it may cause the burglar to stay a shorter amount of time.<br /><br />

<strong><font style="font-size:18px;">What Should a Security Alarm System Include?</font></strong><br />
Most systems rely on a combination of contacts placed at doors and windows and motion sensors.<br />
It is best that all exterior potential points of entry have contacts that sense when they are open. Glass break sensors are also available and recommended.<br />
Motion sensors should not be used as the primary means of detection because they do not detect someone until they are already in the house.<br /><br />

<strong><font style="font-size:18px;">The basic elements of a standard home security system include:</font></strong><br />
• Control panel: This is where the system wiring terminates, the backup battery is located and where it is connected to the phone lines if it is a monitored system. <br />
• Keypad: This is where the system is armed and disarmed. <br />
• Siren<br />
• Inside motion detector: These sense changes in a room caused by human presence. Special motion detectors are available for people who have pets. <br />
• Door and window contacts:; This sounds the alarm when the door or window is opened (and the system is on). <br />
• A central monitoring station (Company): If the system is monitored, and the alarm is set off, the control panel sends a message to a central monitoring station, which is manned 24 hours a day. <br /><br />

After attempting to contact the homeowner, the central monitoring station will contact the police, fire department, or medics.<br /><br />

There is usually a monthly fee for this service. <br /><br />

<strong><font style="font-size:18px;">Additional items that can be added to the basic system are:</font></strong><br />
• Smoke detectors<br />
• Glass break detectors<br />
• Panic buttons<br />
• Pressure mats for under rugs<br />
• Closed circuit TV to allow monitoring and/or recording inside or outside a home. <br />
• Alarm screens for windows. </p>


